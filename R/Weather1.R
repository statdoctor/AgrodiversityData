#' Weather1 data of the Agrodiversity experiment
#'
#'@description 
#'   The \code{weather1} dataset has information about  daily climate data for each site for the duration of the experiment. The climate data series begins on the sowing date of the experiment at each site. The variables include daily temperatures and precipitation.
#'
#' @format A dataframe with 39,712 rows and 9 variables:
#' \describe{
#'   \item{site}{Site ID number}
#'   \item{day}{Day of data collected}
#'   \item{month}{Month of data collected}
#'   \item{year}{Year of data collected}
#'   \item{date}{Date of data collection}
#'   \item{precip}{Daily precipitation (mm/day)}
#'   \item{air_min}{Minimum daily air temperature (°C)}
#'   \item{air_mean}{Mean daily air temperature (°C)}
#'   \item{air_max}{Maximum daily air temperature (°C)}
#'   }
#' @source 
#' Kirwan, L., Connolly, J., Brophy, C., Baadshaug, O.H., Belanger, G., Black, A., Carnus, T., Collins, R., Čop, J., Delgado, I., De Vliegher, A., Elgersma, A., Frankow-Lindberg, B., Golinski, P., Grieu, P., Gustavsson, A.-M., Helgadottir, A., Höglind, M., Huguenin-Elie, O., Jørgensen, M., Kadziulienė, Z., Lunnan, T., Lüscher, A., Kurki P., Porqueddu, C.,  Sebastià, M.T., Thumm, U., Walmsley, D. and Finn, J.A, 2014. The Agrodiversity Experiment: three years of data from a multisite study in intensively managed grasslands. Ecology, 95(9), pp.2680-2680.
#' 
