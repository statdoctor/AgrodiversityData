#' Yearwise Yield data of the Agrodiversity experiment
#'
#'The \code{agrobiosum} dataset provides information about Species-specific biomass measurements (yield per species and of weeds) at 33 sites summarized by year.
#' @format A dataframe with 92 rows and 9 variables:
#' \describe{
#'   \item{SITE}{Site ID number}
#'   \item{Country}{Country}
#'   \item{Year}{Year}
#'   \item{G1_YA}{Average harvest Dry Matter Yield per year of fast-establishing grass}
#'   \item{G2_YA}{Average harvest Dry Matter Yield per year of persistent grass}
#'   \item{L1_YA}{Average harvest Dry Matter Yield per year of fast-establishing legume }
#'   \item{L2_YA}{Average harvest Dry Matter Yield per year of persistent legume }
#'   \item{WEED_YA}{Average harvest Dry Matter Yield per year of weed species }
#'   \item{HARV_YA}{Average harvest Dry Matter Yield per year}
#'}
#' @source <https://repositori.udl.cat/handle/10459.1/60560>
#'
#' @section References:
#' 
#'Kirwan, L., Lüscher, A., Sebastià, M.T., Finn, J.A., Collins, R.P., Porqueddu, C., Helgadottir, A., Baadshaug, O.H., Brophy, C., Coran, C. and Dalmannsdottir, S., 2007. Evenness drives consistent diversity effects in intensive grassland systems across 28 European sites. Journal of Ecology, 95(3), pp.530-539.
#'
#'Kirwan, L., Connolly, J., Brophy, C., Baadshaug, O.H., Belanger, G., Black, A., Carnus, T., Collins, R.P., Čop, J., Delgado, I. and De Vliegher, A., 2014. The Agrodiversity Experiment: three years of data from a multisite study in intensively managed grasslands. Ecology, 2014, vol. 95, num. 9, p. 2680-2680.

